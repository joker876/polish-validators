export class BankNameTree {
  static existsCode(code: string): boolean {
    return (
      'number' ===
      typeof code
        .split('')
        // for every digit
        // traverse the tree up one level
        // if a number is hit return it
        // if an object is hit traverse further into it
        .reduce((obj, digit) => (typeof obj === 'number' ? obj : obj[digit]), BANK_NAME_TREE as BankNameTreeNode)
    );
  }

  static getIndex(code: string): number | null {
    let node: BankNameTreeNode | undefined = BANK_NAME_TREE;

    for (const digit of code) {
      if (typeof node === 'number') {
        return node;
      }
      node = node?.[digit];
      if (node === undefined) {
        return null; // invalid digit path
      }
    }

    return typeof node === 'number' ? node : null;
  }
}
